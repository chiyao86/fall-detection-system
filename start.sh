#!/bin/bash
# Render 啟動腳本 - 確保資料庫遷移在應用程式啟動前完成

echo "=========================================="
echo "執行資料庫初始化..."
echo "=========================================="

# 執行資料庫初始化
python init_db.py

# 檢查初始化是否成功
if [ $? -eq 0 ]; then
    echo "=========================================="
    echo "啟動應用程式..."
    echo "=========================================="
    # 啟動 Gunicorn - 增加 timeout 到 300 秒，處理多張圖片需要更長時間
    exec gunicorn -b 0.0.0.0:$PORT -w 2 --timeout 300 --graceful-timeout 30 run:app
else
    echo "✗ 資料庫初始化失敗，應用程式無法啟動"
    exit 1
fi
